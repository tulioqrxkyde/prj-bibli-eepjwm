    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        if (editora != null) {
            if (editora.getIdEditora() != 0) {
                if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir a Editora " + editora.getNome()
                        + "?", "OSBiblio", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION) {
                    if (editRN.remover(editora)) {
                        JOptionPane.showMessageDialog(rootPane, "Editora " + editora.getNome()
                                + ", excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Não foi possível excluir o autor "
                                + editora.getNome(),
                                "Erro ao Excluir", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
        limpaCampos();
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (editora == null) {
            editora = new Editora();
        }
        if (Util.chkVazio(tfDescricao.getText())) {
            editora.setNome(tfDescricao.getText());
            int id = editora.getIdEditora();
            if (editRN.salvar(editora)) {
                JOptionPane.showMessageDialog(rootPane, "Editora " + editora.getNome()
                        + ", " + ((id == 0) ? "cadastrada" : "alterada") + " com sucesso!");
                limpaCampos();
            }
        }
    }//GEN-LAST:event_btSalvarActionPerformed

