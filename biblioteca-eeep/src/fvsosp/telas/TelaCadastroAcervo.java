        for (String palavra : listaPalavrasChaves) {
            model.add(c, palavra);
            c++;
        }
        listaPalavras.setModel(model);
        if (model.getSize() > 0) {
            scrollPalavras.setVisible(true);
            listaPalavras.setVisible(true);
        } else {
            scrollPalavras.setVisible(false);
        }
